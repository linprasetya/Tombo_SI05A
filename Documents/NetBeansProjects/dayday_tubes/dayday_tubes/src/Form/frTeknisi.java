    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try
        {
            Statement st = (Statement) Connect.GetConnection().createStatement();
            String search = Search.getText();
            ResultSet rs = st.executeQuery("SELECT * FROM teknisi WHERE username like '%"+search+"%' OR bagian like '%"+search+"%'  OR no_telp like '%"+search+"%' OR kode_teknisi like '%"+search+"%'OR lev_akses like '%"+search+"%'");
            DefaultTableModel dtm = (DefaultTableModel) tabel.getModel();

            dtm.setRowCount(0);
            String [] data = new String[6];
            int i = 1;

            while(rs.next())
            {
                data[0] = rs.getString("kode_teknisi");
                data[1] = rs.getString("username");
                data[2] = rs.getString("password");
                data[3] = rs.getString("lev_akses");
                data[4] = rs.getString("bagian");
                data[5] = rs.getString("no_telp");
                dtm.addRow(data);
                i++;
            }
            rs.close();
            Search.setText("");
        }
        catch(Exception ex)  {
            JOptionPane.showMessageDialog(null,"Data yang Anda cari Tidak dapat Ditemukan");
            System.err.println("error (search) : " +ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

