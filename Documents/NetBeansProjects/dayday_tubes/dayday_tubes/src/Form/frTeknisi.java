    public void loadData(String idt,String login){
        DefaultTableModel models=new DefaultTableModel();
        models.addColumn("Kode");
        models.addColumn("Username");
        models.addColumn("Password");
        models.addColumn("Level Akses");
        models.addColumn("Bagian / Divisi");
        models.addColumn("No Telp");

        tabel.setModel(models);
        try {
        Statement statement=(Statement)Connect.GetConnection().createStatement();
        if("Teknisi".equals(login)){
            ResultSet res=statement.executeQuery("select * from teknisi where kode_teknisi = '"+idt+"'");
            while(res.next())
            {
            models.addRow(new Object[]{
            res.getString("kode_teknisi"),
            res.getString("username"),
            res.getString("password"),
            res.getString("lev_akses"),
            res.getString("bagian"),
            res.getString("no_telp"),
            });
            tabel.setModel(models);
            }
        }else{
            ResultSet res=statement.executeQuery("select * from teknisi");
            while(res.next())
            {
            models.addRow(new Object[]{
            res.getString("kode_teknisi"),
            res.getString("username"),
            res.getString("password"),
            res.getString("lev_akses"),
            res.getString("bagian"),
            res.getString("no_telp"),
            });
            tabel.setModel(models);
            }
        }
        }catch (Exception e){
        JOptionPane.showMessageDialog(rootPane,"Tidak Ada Data");
        }
    }
    

