    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        String kode = kd.getText();
        String nama = username.getText();
        String jabatan = (String)level.getSelectedItem();
        String jenis = password.getText();
        String bagian = txtbagian.getText();
        String no_telp = txtnotelp.getText();
        try{
                Statement stm = (Statement) Connect.GetConnection().createStatement();
                stm.execute("UPDATE teknisi SET "
                    + "username = '" + nama + "',"
                    + "password = '" + jenis + "',"
                    + "lev_akses = '" + jabatan + "',"
                    + "bagian = '" + bagian + "', "
                    + "no_telp = '" + no_telp + "' "
                    + "WHERE kode_teknisi = '" + kode + "';");
                stm.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!","SIMPAN DATA",JOptionPane.INFORMATION_MESSAGE);
            }
            catch (Exception t){
                JOptionPane.showMessageDialog(null, "Edit data Gagal Disimpan!","SIMPAN DATA",JOptionPane.INFORMATION_MESSAGE);
            }
            loadData(kode, jabatan);
    }//GEN-LAST:event_editActionPerformed

